public class loop
{
    public static void main(String[] args) 
    {
        //for loop
        for(int i=1;i<5;i++)
        {
            System.out.println("Java");
        }

        //while loop
        int a=0;
        while(a<5)
        {
            System.out.println("hello");
            a++;
        }

        //Do while
        int b=0;
        do
        {
          System.out.println("heyyy!!");
          b++;
        }while(b<5);
        
    }
}
